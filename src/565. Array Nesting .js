    let len = nums.length

    let maxLen = 0;

    for (let i = 0; i < len; ++i) {
        let val = nums[i]
        let count = 0
        while (val < len && !hash[val]) {
            hash[val] = true;
            count++
            val = nums[val]
        }

        if (maxLen < count) {
            maxLen = count;
        }
    }

    return maxLen
