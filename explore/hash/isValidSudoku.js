/**
 * @param {character[][]} board
 * @return {boolean}
 */
var isValidSudoku = function(board) {
    var row = board.length;
    var col = board[0].length;

    for (var i = 0; i < row; ++i) {
        var line = new Set();
        for (var j = 0; j < col; ++j) {
            var c = board[i][j];
            if (c !== ".") {
                if (line.has(c)) {
                    return false;
                } else {
                    line.add(c);
                }
            }
        }
    }

    for (var i = 0; i < col; ++i) {
        var line = new Set();
        for (var j = 0; j < row; ++j) {
            var c = board[j][i];
            if (c !== ".") {
                if (line.has(c)) {
                    return false;
                } else {
                    line.add(c);
                }
            }
        }
    }

    for (var i = 0; i < row; i += 3) {
        for (var j = 0; j < col; j += 3) {
            var cube = new Set();
            for (var m = i; m < i + 3; ++m) {
                for (var n = j; n < j + 3; ++n) {
                    var c = board[m][n];
                    if (c !== ".") {
                        if (cube.has(c)) {
                            return false;
                        } else {
                            cube.add(c);
                        }
                    }

                }
            }
        }
    }
    return true;
};


